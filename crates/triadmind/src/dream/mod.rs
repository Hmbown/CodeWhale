//! # Dream — Background architecture health analysis ("Dream Engine")
//!
//! Ported from triadmind-core/dream.ts
//!
//! Runs asynchronously in the background to detect architecture rot:
//! - Abstraction deficit hotspots (too many concrete methods)
//! - High fan-out nodes (too many downstream dependencies)
//! - Ghost nodes (missing implementations / unverified dependencies)
//! - Execute-like ratio violations
//!
//! Generates `DreamProposal` suggestions that the user can accept or reject.
//! Rejected proposals feed back into the system as architecture constraints.
//!
//! @LeftBranch: run_dream_analysis, generate_proposals
//! @RightBranch: DreamReport, DreamFinding, DreamProposal, DreamState

