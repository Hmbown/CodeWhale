// ── Helpers ─────────────────────────────────────────────────────────

fn ratio_confidence(actual: f64, threshold: f64) -> f64 {
    let delta = (actual - threshold).max(0.0);
    let normalized = if threshold <= 0.0 { delta } else { delta / threshold };
    (0.6 + normalized * 0.25).min(0.95)
}

