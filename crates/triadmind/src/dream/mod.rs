// ── Constants ───────────────────────────────────────────────────────

/// Prefix for ghost demand entries in node dependencies.
const GHOST_DEMAND_PREFIX: &str = "[Ghost:";

/// Fan-out threshold for high-dependency alerts.
const FANOUT_ALERT_THRESHOLD: usize = 6;

// ── Run Entry Point ─────────────────────────────────────────────────

/// Run the dream engine analysis.
///
/// Loads the triad-map.json, computes topology metrics, detects architecture
/// issues (execute-like ratio, ghost ratio, empty vertices, high fan-out),
/// and generates refactoring proposals.
pub fn run_dream_analysis(
    project_root: &Path,
    map_file: &Path,
    options: &DreamRunOptions,
) -> Result<DreamReport, anyhow::Error> {
    let project = project_root
        .file_name()
        .unwrap_or(project_root.as_os_str())
        .to_string_lossy()
        .to_string();

    let now = crate::sync::chrono_now();

    // ── Load triad-map ────────────────────────────────────────────
    let nodes: Vec<crate::protocol::TriadNodeDefinition> = if map_file.exists() {
        let content = std::fs::read_to_string(map_file)?;
        let trimmed = content.trim().trim_start_matches('\u{FEFF}');
        serde_json::from_str(trimmed).unwrap_or_default()
    } else {
        Vec::new()
    };

    if nodes.is_empty() {
        return Ok(DreamReport {
            schema_version: "1.0".into(),
            project,
            generated_at: now,
            mode: options.mode,
            skipped: true,
            skip_reason: Some("No triad-map.json found or map is empty".into()),
            config: DreamConfig::default(),
            metrics: DreamMetricsSnapshot {
                triad_nodes: 0,
                execute_like_ratio: 0.0,
                ghost_ratio: 0.0,
                empty_vertices: 0,
            },
            findings: Vec::new(),
            proposals: Vec::new(),
            diagnostics: Vec::new(),
            summary: vec!["No topology data to analyze.".into()],
        });
    }

    // ── Compute metrics ───────────────────────────────────────────
    let metrics = crate::verify::compute_verify_metrics(&nodes);

    // ── Detect findings ───────────────────────────────────────────
    let mut findings: Vec<DreamFinding> = Vec::new();
    let mut finding_id = 0u32;

    // Check execute-like ratio
    if metrics.execute_like_ratio > 0.1 {
        finding_id += 1;
        findings.push(DreamFinding {
            id: format!("F{:03}", finding_id),
            finding_type: DreamFindingType::Metric,
            severity: if metrics.execute_like_ratio > 0.3 {
                DreamSeverity::Error
            } else {
                DreamSeverity::Warning
            },
            title: "High execute-like ratio".into(),
            description: format!(
                "{} of {} nodes ({:.1}%) have execute-like method names. \
                 Consider renaming to domain-specific verbs or splitting \
                 overly generic handlers.",
                metrics.execute_like_count,
                metrics.triad_nodes,
                metrics.execute_like_ratio * 100.0,
            ),
            metric: Some("execute_like_ratio".into()),
            current_value: Some(serde_json::json!(metrics.execute_like_ratio)),
            target_value: Some(serde_json::json!(0.1)),
            confidence: ratio_confidence(metrics.execute_like_ratio, 0.1),
            evidence: vec![DreamEvidence {
                evidence_type: "metric".into(),
                key: "execute_like_count".into(),
                value: metrics.execute_like_count.to_string(),
                source_path: None,
            }],
        });
    }

    // Check ghost ratio
    if metrics.ghost_ratio > 0.4 {
        finding_id += 1;
        let ghost_count = metrics.ghost_nodes;
        findings.push(DreamFinding {
            id: format!("F{:03}", finding_id),
            finding_type: DreamFindingType::Topology,
            severity: if metrics.ghost_ratio > 0.6 {
                DreamSeverity::Error
            } else {
                DreamSeverity::Warning
            },
            title: "High ghost node ratio".into(),
            description: format!(
                "{} nodes ({:.1}%) have ghost dependencies. \
                 These represent unresolved or unverified external dependencies.",
                ghost_count,
                metrics.ghost_ratio * 100.0,
            ),
            metric: Some("ghost_ratio".into()),
            current_value: Some(serde_json::json!(metrics.ghost_ratio)),
            target_value: Some(serde_json::json!(0.4)),
            confidence: ratio_confidence(metrics.ghost_ratio, 0.4),
            evidence: vec![DreamEvidence {
                evidence_type: "metric".into(),
                key: "ghost_nodes".into(),
                value: ghost_count.to_string(),
                source_path: None,
            }],
        });
    }

    // Check empty vertices
    if metrics.empty_vertices > 0 {
        finding_id += 1;
        findings.push(DreamFinding {
            id: format!("F{:03}", finding_id),
            finding_type: DreamFindingType::Topology,
            severity: if metrics.empty_vertices > 5 {
                DreamSeverity::Warning
            } else {
                DreamSeverity::Info
            },
            title: "Empty vertices detected".into(),
            description: format!(
                "{} nodes have no problem/demand/answer defined. \
                 These nodes lack the TriadMind fission triple and should \
                 be annotated or removed.",
                metrics.empty_vertices,
            ),
            metric: Some("empty_vertices".into()),
            current_value: Some(serde_json::json!(metrics.empty_vertices)),
            target_value: Some(serde_json::json!(0)),
            confidence: 0.8,
            evidence: Vec::new(),
        });
    }

    // Check high fan-out: find nodes referenced as demand by many others
    let fan_out = compute_fan_out(&nodes);
    let high_fanout_nodes: Vec<_> = fan_out
        .iter()
        .filter(|(_, count)| *count >= FANOUT_ALERT_THRESHOLD)
        .collect();

    if !high_fanout_nodes.is_empty() {
        finding_id += 1;
        let worst = high_fanout_nodes.first().unwrap();
        findings.push(DreamFinding {
            id: format!("F{:03}", finding_id),
            finding_type: DreamFindingType::Topology,
            severity: DreamSeverity::Warning,
            title: "High fan-out nodes".into(),
            description: format!(
                "{} nodes have {} or more downstream dependents. \
                 Top offender: '{}' with {} dependents. \
                 Consider introducing an abstraction layer.",
                high_fanout_nodes.len(),
                FANOUT_ALERT_THRESHOLD,
                worst.0,
                worst.1,
            ),
            metric: Some("max_fan_out".into()),
            current_value: Some(serde_json::json!(worst.1)),
            target_value: Some(serde_json::json!(FANOUT_ALERT_THRESHOLD - 1)),
            confidence: 0.75,
            evidence: high_fanout_nodes
                .iter()
                .take(5)
                .map(|(name, count)| DreamEvidence {
                    evidence_type: "node".into(),
                    key: format!("fan_out_{}", name),
                    value: count.to_string(),
                    source_path: None,
                })
                .collect(),
        });
    }

    // ── Generate proposals ────────────────────────────────────────
    let proposals = generate_proposals(&findings, options, &nodes);

    // ── Build summary ─────────────────────────────────────────────
    let mut summary = Vec::new();
    summary.push(format!(
        "Analyzed {} triad nodes: execute-like={:.1}%, ghost={:.1}%, empty={}",
        metrics.triad_nodes,
        metrics.execute_like_ratio * 100.0,
        metrics.ghost_ratio * 100.0,
        metrics.empty_vertices,
    ));
    summary.push(format!(
        "Findings: {} | Proposals: {}",
        findings.len(),
        proposals.len(),
    ));
    if !proposals.is_empty() {
        summary.push(format!(
            "Top proposal: {} (confidence={:.2})",
            proposals[0].title,
            proposals[0].confidence,
        ));
    }

    Ok(DreamReport {
        schema_version: "1.0".into(),
        project,
        generated_at: now,
        mode: options.mode,
        skipped: false,
        skip_reason: None,
        config: DreamConfig::default(),
        metrics: DreamMetricsSnapshot {
            triad_nodes: metrics.triad_nodes,
            execute_like_ratio: metrics.execute_like_ratio,
            ghost_ratio: metrics.ghost_ratio,
            empty_vertices: metrics.empty_vertices,
        },
        findings,
        proposals,
        diagnostics: Vec::new(),
        summary,
    })
}

// ── Fan-out Computation ─────────────────────────────────────────────

/// Compute how many other nodes reference each node as a demand/dependency.
fn compute_fan_out(nodes: &[crate::protocol::TriadNodeDefinition]) -> Vec<(String, usize)> {
    let mut fan_map: std::collections::HashMap<String, usize> = std::collections::HashMap::new();

    for node in nodes {
        if let Some(ref fission) = node.fission {
            for demand in &fission.demand {
                // Skip ghost demands and generic contracts
                if demand.starts_with(GHOST_DEMAND_PREFIX) {
                    continue;
                }
                *fan_map.entry(demand.clone()).or_insert(0) += 1;
            }
        }
    }

    let mut sorted: Vec<(String, usize)> = fan_map.into_iter().collect();
    sorted.sort_by(|a, b| b.1.cmp(&a.1));
    sorted
}

// ── Proposal Generation ─────────────────────────────────────────────

/// Generate refactoring proposals from detected findings.
fn generate_proposals(
    findings: &[DreamFinding],
    options: &DreamRunOptions,
    nodes: &[crate::protocol::TriadNodeDefinition],
) -> Vec<DreamProposal> {
    let mut proposals = Vec::new();
    let mut prop_id = 0u32;

    for finding in findings {
        if finding.confidence < options.min_confidence {
            continue;
        }

        prop_id += 1;
        let proposal = match finding.metric.as_deref() {
            Some("execute_like_ratio") => make_execute_like_proposal(prop_id, finding, nodes),
            Some("ghost_ratio") => make_ghost_proposal(prop_id, finding, nodes),
            Some("empty_vertices") => make_empty_vertices_proposal(prop_id, finding),
            Some("max_fan_out") => make_fanout_proposal(prop_id, finding, nodes),
            _ => continue,
        };

        proposals.push(proposal);

        if proposals.len() >= options.max_proposals {
            break;
        }
    }

    // Sort by confidence descending
    proposals.sort_by(|a, b| b.confidence.partial_cmp(&a.confidence).unwrap_or(std::cmp::Ordering::Equal));

    proposals
}

fn make_execute_like_proposal(
    id: u32,
    finding: &DreamFinding,
    _nodes: &[crate::protocol::TriadNodeDefinition],
) -> DreamProposal {
    DreamProposal {
        id: format!("P{:03}", id),
        title: "Reduce execute-like naming".into(),
        priority: DreamPriority::Medium,
        confidence: finding.confidence,
        category: None,
        source_path: None,
        objective: "Replace generic execute/run/handle method names with domain-specific verbs".into(),
        expected_outcome: "Improved code readability and clearer architecture boundaries".into(),
        actions: vec![
            "Audit all execute/run/handle methods".into(),
            "Rename to domain-specific verbs (e.g., 'processOrder', 'validatePayment')".into(),
            "Split oversized handler classes by responsibility".into(),
        ],
        linked_findings: vec![finding.id.clone()],
        evidence: finding.evidence.clone(),
        protocol_draft: None,
    }
}

fn make_ghost_proposal(
    id: u32,
    finding: &DreamFinding,
    nodes: &[crate::protocol::TriadNodeDefinition],
) -> DreamProposal {
    let ghost_count = nodes
        .iter()
        .filter(|n| {
            n.fission
                .as_ref()
                .map(|f| f.demand.iter().any(|d| d.starts_with(GHOST_DEMAND_PREFIX)))
                .unwrap_or(false)
        })
        .count();

    DreamProposal {
        id: format!("P{:03}", id),
        title: "Resolve ghost dependencies".into(),
        priority: DreamPriority::High,
        confidence: finding.confidence,
        category: None,
        source_path: None,
        objective: format!("Resolve or remove {} ghost dependency references", ghost_count),
        expected_outcome: "Clean dependency graph with only verified edges".into(),
        actions: vec![
            "Run 'triadmind sync --force' to rescan source files".into(),
            "Replace [Ghost:xxx] references with actual type names".into(),
            "Remove references to deleted or moved modules".into(),
        ],
        linked_findings: vec![finding.id.clone()],
        evidence: finding.evidence.clone(),
        protocol_draft: None,
    }
}

